package com.example;

public class Person {
    String name;
    int age;

    // Constructor
    public Person(String name,int age) {
        this.name = name;
        this.age = age;
    }

    public static void main(String[] args) {
        Person person = new Person("John",25);
        System.out.println("Name: " + person.name);
        System.out.println("Age: " + person.age);
    }

    
}
